import java.util.*;
public class DataTypes
{
    public static void  main()
    {
        byte x=2;
        int y=10;
        char c='d';
        long xy=123456;
        float xyz=123.4f;
        double xd =125.5645;
        String str="Shibam";
        System.out.println("The byte value is = "+x);
        System.out.println("The integer value is = "+y);
        System.out.println("The character value is = "+c);
        System.out.println("The long value is = "+xy);
        System.out.println("The float value is = "+xyz);
        System.out.println("The double value is = "+xd);
        System.out.println("The String value is = "+str);
    }
}
